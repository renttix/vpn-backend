// Function to create or update the SEO guide document
export async function createSeoGuideDocument(client) {
  try {
    // Check if the document already exists
    const existingDoc = await client.getDocument('seoGuide');
    
    if (!existingDoc) {
      // Create the document if it doesn't exist
      await client.create(seoGuideContent);
      console.log('SEO Guide document created successfully');
    } else {
      // Update the document if it exists
      await client.createOrReplace(seoGuideContent);
      console.log('SEO Guide document updated successfully');
    }
  } catch (error) {
    console.error('Error creating/updating SEO Guide document:', error);
  }
}
